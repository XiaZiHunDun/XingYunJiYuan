def truncate_to_paragraph(text, target_chars):
    """将文本截断到目标字符数，在段落边界或句号处结束"""
    # 首先按段落分割
    paragraphs = text.split('\n\n')

    result_paragraphs = []
    current_count = 0

    for para in paragraphs:
        para_chars = len(re.findall(r'[\u4e00-\u9fff\u3000-\u303f\uff00-\uffef]', para))

        if current_count + para_chars > target_chars:
            # 这段会超出，尝试截断当前段落
            remaining = target_chars - current_count
            if remaining <= 0:
                break

            # 按句子截断
            sentences = re.split(r'([。！？])', para)
            truncated = []
            count = 0
            for i, sent in enumerate(sentences):
                sent_c = len(re.findall(r'[\u4e00-\u9fff\u3000-\u303f\uff00-\uffef]', sent))
                if count + sent_c > remaining:
                    break
                truncated.append(sent)
                count += sent_c

            # 合并句子
            combined = ''.join(truncated)
            # 确保以句号结尾
            if combined and combined[-1] not in '。！？':
                # 找最后一个可接受的结束点
                for punct in ['。', '！', '？']:
                    last = combined.rfind(punct)
                    if last > len(combined) - 30:
                        combined = combined[:last+1]
                        break
            result_paragraphs.append(combined)
            break
        else:
            result_paragraphs.append(para)
            current_count += para_chars

    return '\n\n'.join(result_paragraphs)

def process_chapter(file_path, target_count=2500):
    """处理单个章节"""
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()

    # 提取主要剧情内容（截止到**本章完**）
    main_content = extract_main_content(content)

    # 统计当前字数
    current_count = count_chinese_chars(main_content)

    if current_count >= target_count:
        print(f"already ≥{target_count}, skipping")
        return 0

    # 需要扩充的字符数
    needed = target_count - current_count

    # 获取扩写内容
    all_blocks = create_expansion_content()

    # 选择足够的块来达到目标
    selected_blocks = []
    total_chars = 0

    for block in all_blocks:
        block_chars = count_chinese_chars(block)
        if total_chars + block_chars >= needed:
            remaining = needed - total_chars
            if remaining > 0 and remaining < block_chars:
                truncated_block = truncate_to_paragraph(block, remaining)
                if truncated_block:
                    selected_blocks.append(truncated_block)
                    total_chars += count_chinese_chars(truncated_block)
            break
        selected_blocks.append(block)
        total_chars += block_chars

    # 合并扩写内容
    expansion = "\n\n".join(selected_blocks)

    # 构建新内容：在主内容后添加扩写
    new_main_content = main_content + "\n\n" + expansion

    # 构建文件其余部分：只用**本章完**作为结尾
    new_after_marker = "\n\n**本章完**"

    # 构建完整的新内容
    new_file_content = new_main_content + new_after_marker

    # 写入文件
    with open(file_path, 'w', encoding='utf-8') as f:
        f.write(new_file_content)

    new_count = count_chinese_chars(new_main_content)
    added = new_count - current_count
    print(f"chars: {current_count} -> {new_count} (added {added})")

    return added

